/**
 * The type Hello world.
 */
public class HelloWorld {
    /**
     * The entry point of application.
     *
     * @param args the input arguments
     */
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }

    /**
     * Add int.
     *
     * @param a the a
     * @param b the b
     * @return the int
     */
    public int add(int a, int b) {
        return a + b;
    }
}
